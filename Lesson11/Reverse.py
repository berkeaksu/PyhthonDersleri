# .reverse() => elinizde var olan dizi içerisindeki nesneleri sıralama yapmadan tersine cevirme işlemi yapar.

sayilar = [1,2,3,4,5,6,7,8,9,0]
karakterler = ['a', 'b', 'c', 'd', 'e', 'f']

sayilar.reverse()
karakterler.reverse()

print(sayilar)
print(karakterler)

#[0, 9, 8, 7, 6, 5, 4, 3, 2, 1]
#['f', 'e', 'd', 'c', 'b', 'a']

